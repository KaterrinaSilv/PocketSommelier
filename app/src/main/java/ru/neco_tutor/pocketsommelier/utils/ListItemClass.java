package ru.neco_tutor.pocketsommelier.utils;

public class ListItemClass {
    String nameWine;
    String secondNameWine;
    int image;


    public String getNameWine() {
        return nameWine;
    }

    public void setNameWine(String nameWine) {
        this.nameWine = nameWine;
    }

    public String getSecondNameWine() {
        return secondNameWine;
    }

    public void setSecondNameWine(String secondNameWine) {
        this.secondNameWine = secondNameWine;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
